    '          const cfg = gosiPolicy.config as any;\n'
    '          gosiEmpRateSaudi    = (cfg.employee_rate           || 9.75) / 100;\n'
    '          gosiErRateSaudi     = (cfg.employer_rate           || 11.75) / 100;\n'
    '          gosiErRateNonSaudi  = (cfg.non_saudi_employer_rate || 2.0)  / 100;\n'
    '          maxInsurable        = cfg.max_insurable_salary     || 45000;\n'
    '          minInsurable        = cfg.min_insurable_salary     || 1500;\n'
    '        }\n'
    '      } catch (_) { /* use defaults */ }\n'
    '\n'
    '      // \u2500\u2500 2. All employees (with nationality for GOSI) \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n'
    '      const employees = await sql' + bt + '\n'
    '        SELECT id, name, department, nationality,\n'
    '               basic_salary, housing_allowance, transport_allowance\n'
    '        FROM employees\n'
    '      ' + bt + ';\n'
    '\n'
    '      // \u2500\u2500 3. Active loan deductions per employee \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n'
    '      const loanRows = await sql' + bt + '\n'
    '        SELECT employee_id, loan_type, monthly_installment\n'
    '        FROM loans\n'
    '        WHERE status IN (' + chr(39) + 'active' + chr(39) + ', ' + chr(39) + 'approved' + chr(39) + ') AND installments_left > 0\n'
    '      ' + bt + ';\n'
    '      type LoanMap = Record<string, { loan: number; advance: number }>;\n'
    '      const loanMap: LoanMap = {};\n'
    '      for (const lr of loanRows) {\n'
    '        const eid = lr.employee_id as string;\n'
    '        if (!loanMap[eid]) loanMap[eid] = { loan: 0, advance: 0 };\n'
    '        const isAdvance = String(lr.loan_type).toLowerCase().includes("advance");\n'
    '        if (isAdvance) {\n'
    '          loanMap[eid].advance += Number(lr.monthly_installment) || 0;\n'
    '        } else {\n'
    '          loanMap[eid].loan += Number(lr.monthly_installment) || 0;\n'
    '        }\n'
    '      }\n'
    '\n'
    '      // \u2500\u2500 4. Paycodes \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n'
    '      const paycodes = await sql' + bt + 'SELECT * FROM paycodes WHERE is_active = true ORDER BY sort_order' + bt + ';\n'
    '\n'
    '      // \u2500\u2500 5. Create payroll run header \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n'
    '      const [run] = await sql' + bt + '\n'
    '        INSERT INTO payroll_runs (ref, period_label, period_month, period_year, employee_count, notes)\n'
    '        VALUES (${ref}, ${periodLabel}, ${period_month}, ${period_year}, ${employees.length}, ${notes || ""})\n'
    '        RETURNING *\n'
    '      ' + bt + ';\n'
    '\n'
    '      let totalGross = 0, totalDeductions = 0, totalGosiEmp = 0, totalGosioEr = 0;\n'
    '\n'
    '      // \u2500\u2500 6. Build payroll lines per employee \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n'
    '      for (const emp of employees) {\n'
