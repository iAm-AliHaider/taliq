    '        const amountMap: Record<string, number> = {\n'
    '          BASIC:     basic,\n'
    '          HOUSING:   housing,\n'
    '          TRANSPORT: transport,\n'
    '          GOSI_EMP:  gosiEmp,\n'
    '          GOSI_CO:   gosiEr,\n'
    '          LOAN_DED:  loanDed,\n'
    '          ADV_DED:   advanceDed,\n'
    '        };\n'
    '\n'
    '        for (const pc of paycodes) {\n'
    '          const amount = amountMap[pc.code as string];\n'
    '          if (amount === undefined || amount === 0) continue;\n'
